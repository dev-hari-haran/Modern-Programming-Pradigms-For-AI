import numpy as np   # Import NumPy																									
																								
arr3 = np.array([[1, 2, 3], [4, 5, 6]])																									
																									
print("Original Array:\n", arr3)																									
print("Array * Array:\n", arr3 * arr3)																									
print("Array - Array:\n", arr3 - arr3)																									
print("Reciprocal (1/array):\n", 1 / arr3)																									
print("Array power 0.5:\n", arr3 ** 0.5)																									
																									
